#Toma un número entero y determina si es positivo o negativo
numero = int(input("Ingrese un número entero: ")) 

#Si el número es mayor o igual a 0, es positivo
if numero >= 0:
  #Imprime el numero es Positivo.
  print("El número es positivo")

#Si el número es menor a 0, es negativo
elif numero < 0:
  #Imprime el numero es negativo
  print("El número es negativo")

else: numero == 0
#imprimee el numero es 0
print("El número es 0, por lo tanto es nulo")

